const int M = 1000000007;
 
LL fact[inf+1];
 
void precal() {
    fact[0]=1;
    for (int i=1; i<=inf; i++)
        fact[i]=((fact[i-1]%M)*(i%M))%M;
}
 
LL bigmod (LL b, LL p, LL m)
{
    if (p==0) return 1;
    if (p%2==0) {
        LL x=bigmod(b, p/2, m)%m;
        return (x*x)%m;
    }
    else return (b%m * bigmod(b, p-1, m))%m;
}
 
LL inv_mod(LL a, LL m) {
    return bigmod(a, m-2, m);
}
 
LL solve(LL n, LL k) {
    // cout<<inv_mod(fact[k], M)<<" "<<inv_mod(fact[n-k], M)<<endl;
    LL now = (fact[n]%M * inv_mod(fact[k], M))%M;
    now = (now%M * inv_mod(fact[n-k], M))%M;
    return now;
}


std::vector<int> v[MAX+1];

void precal() {
    v[1].push_back(1);
    v[2] = {1, 2};
    for (int i=3; i<=MAX; i++) {
        v[i].push_back(1);
        for (int j=2; j*j<=i; j++) {
            if (i%j==0) {
                if (j == i/j)
                    v[i].push_back(j);
                else {
                    v[i].push_back(j);
                    v[i].push_back((i/j));
                }
            }
        }
        v[i].push_back(i);
    }
}

bool vis[MAX+1];
double dp[MAX+1];

double solve(int n) {
    if (n==1)
        return 0;
    if (vis[n])
        return dp[n];
    double d = v[n].size();
    if (d==1)
        return 1;
    dp[n] = (d/(d-1.0));
    for (auto i: v[n]) {
        if (i==n) continue;
        dp[n] += double(solve(i))/(d-1.0);
    }
    vis[n] = 1;
    return dp[n];
}


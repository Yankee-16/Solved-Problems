LL mx = LL(1e12);
vector<LL> lucky;
map<LL, bool> visited;

void precal() {
	lucky.push_back(4);
	lucky.push_back(7);
  visited[4] = visited[7] = 1;
	LL i=0;
	while (lucky[i]<=mx) {
		LL x = lucky[i]*10LL + 4;
		LL y = lucky[i]*10LL + 7;
    if (!visited[x])
		  lucky.push_back(x);
		if (!visited[y])
      lucky.push_back(y);
    visited[x] = visited[y] = 1;
		i++;
	}
	// cout<<lucky.size()<<endl;
	// pvec(lucky);
	for (int i=0; i<lucky.size(); i++) {
		for (int j=0; j<lucky.size(); j++) {
			LL x = lucky[i] * lucky[j];
      if (x>mx)
        break;
      if (!visited[x])
			 lucky.push_back(x);
      visited[x] =1 ;
		}
	}
  sort(lucky.begin(), lucky.end());
	// cout<<lucky.size()<<endl;
}

